    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed

        if (DataBase.verificarUser(instanciaConexion, LblUsuario, LblContraseña)) {
            JOptionPane.showMessageDialog(rootPane, "Usuario Ingresado Correctamente!");
            PaginaPrincipal pP = new PaginaPrincipal();
            pP.setVisible(true);
            pP.setLocationRelativeTo(null);
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "El usuario o la contraseña son incorrectos");
        }


    }//GEN-LAST:event_BtnIngresarActionPerformed

